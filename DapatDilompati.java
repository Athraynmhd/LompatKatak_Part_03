/**
 * Interface DapatDilompati adalah kontrak untuk objek yang dapat melompat
 * baik dekat maupun jauh.
 */
public interface DapatDilompati {
    /**
     * Metode ini digunakan untuk melompat dekat.
     */
    void loncatDekat();

    /**
     * Metode ini digunakan untuk melompat jauh.
     */
    void loncatJauh();
}